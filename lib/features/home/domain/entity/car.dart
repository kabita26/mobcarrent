class Car {
  final String id;
  final String name;
  final String brand;
  final String model;
  final int year;
  final double regularPrice;
  final String description;
  final List<String> imageUrls;

  Car({
    required this.id,
    required this.name,
    required this.brand,
    required this.model,
    required this.year,
    required this.regularPrice,
    required this.description,
    required this.imageUrls,
  });
}
